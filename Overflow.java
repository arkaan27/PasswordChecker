public class Overflow {
    public static void main(String[] args){
        byte x= 14;
        char myChar= 'Y';
        double myDouble= 2.5d;
        int m= 307;
        byte n= -1;
        long l= 438000000L;
        boolean k= false;
        System.out.println(x);
        System.out.println(myChar + "Hello");
        System.out.println(myDouble);
        System.out.println(m +  " " + n + " " + l);
        System.out.println(k);
    }
}
